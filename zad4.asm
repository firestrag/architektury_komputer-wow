section .text;												*
;-----------------------------------------------------------*
global main;												*
main:;														*
_loop:;														*
	call _pobierz;											*
	mov al, [znak];											*
	cmp al, 'k';											*
	jne _loop;;												*
	jmp _exit;												*
;************************************************************


;************************************************************
_pobierz:;													*
	mov rax, 3;												*
	mov rbx, 1;												*
	mov rcx, znak;											*
	mov rdx, 1;												*
	int 80h;												*
	ret;													*
;************************************************************

;************************************************************
_exit:;														*
	mov rax, 1;												*
	mov rbx, 0;												*
	int 80h;												*
;************************************************************

;************************************************************
section .data;												*
;-----------------------------------------------------------*
	znak: db 0;												*
;************************************************************
